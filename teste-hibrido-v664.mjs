import fs from 'fs';
import assert from 'node:assert/strict';
const html=fs.readFileSync(new URL('./index.html',import.meta.url),'utf8');
const app=fs.readFileSync(new URL('./app.js',import.meta.url),'utf8');
const css=fs.readFileSync(new URL('./styles.css',import.meta.url),'utf8');
const sw=fs.readFileSync(new URL('./service-worker.js',import.meta.url),'utf8');
const home=html.slice(html.indexOf('<section id="home"'),html.indexOf('<section id="zip"'));
const rel=html.slice(html.indexOf('<section id="relatorio"'),html.indexOf('<section id="carteira"'));
assert.ok(home.includes('id="cpLeadWorkspace"'),'Home precisa conter o workspace operacional');
assert.ok(!home.includes('id="cpDashboard"'),'Dashboard não deve ocupar a Home');
assert.ok(rel.includes('id="cpDashboard"'),'Dashboard deve ficar em Desempenho');
assert.ok(html.includes('>Hoje</button>'),'Menu precisa ter Hoje');
assert.ok(html.includes('>Propostas</button>'),'Menu precisa chamar Propostas');
assert.ok(!html.includes('>Atividades</button>')&&!html.includes('>Tarefas</button>'),'Menu não pode duplicar Agenda');
assert.ok(!app.includes("url('/avatar-")&&!sw.includes('/avatar-'),'Avatares não podem referenciar arquivos ausentes');
assert.ok(!app.includes('["Ligações"')&&!app.includes('["E-mails"'),'Dashboard não pode inventar ligações/e-mails');
assert.ok(css.includes('ATUALIZAÇÃO #664'),'CSS final da versão 664 ausente');
console.log('Teste híbrido 664: OK — Home operacional, dashboard em Desempenho e métricas reais.');
